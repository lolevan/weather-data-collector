def degrees_to_cardinal(degrees):
    """
    Преобразует направление ветра из градусов в кардинальное направление.
    """
    dirs = ['С', 'ССВ', 'СВ', 'ВСВ', 'В', 'ВЮВ', 'ЮВ', 'ЮЮВ',
            'Ю', 'ЮЮЗ', 'ЮЗ', 'ЗЮЗ', 'З', 'ЗСЗ', 'СЗ', 'ССЗ']
    ix = int((degrees + 11.25) / 22.5)
    return dirs[ix % 16]


def hpa_to_mmhg(hpa):
    """
    Конвертирует давление из гектопаскалей в миллиметры ртутного столба.
    """
    return hpa * 0.75006375541921


